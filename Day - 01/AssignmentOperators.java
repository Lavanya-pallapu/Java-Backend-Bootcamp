public class AssignmentOperators {
    
    public static void main(String[] args){

        int number = 10;

        number += 3;
        System.out.println(number);

        number -= 4;
        System.out.println(number);

        number *= 5;
        System.out.println(number);

        number /= 6;
        System.out.println(number);

        number %= 7;
        System.out.println(number);

    }
}
